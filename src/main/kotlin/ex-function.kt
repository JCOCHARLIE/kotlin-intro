fun main(args: Array<String>) {
    addition(300, 500)
    multiply(200,10)
    divide(100, 5)
}

fun addition(num1:Int, num2:Int){
    var result = num1+num2
    println(result)
}

fun multiply(num3:Int, num4:Int){
    var result = num3*num4
    println(result)
}

fun divide(num5:Int, num6:Int){
    var result = num5/num6
    println(result)
}